package sist;

public class Ex02 {
	
	public static void main(String[] args) {
		System.out.println("프로그램 시작");
		System.out.print("JAVA\t");
		System.out.print("programming");
		System.out.print("\n프로그램 종료");
		
		/*
		 * 제어문자
		 * - \n : 줄바꿈을 제공하는 제어문자.
		 * - \t : 일정 간격(tab)만큼 칸을 띄어주는 제어문자.
		 * 
		 * 이클립스 단축키
		 * - main + ctrl + space : main() 메서드 생성하는 단축키.
		 * - ctrl + shift + f : 들여쓰기 기능 제공하는 단축키.
		 */
		
	}
}
